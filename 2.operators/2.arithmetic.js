// Arithmetic operators
// + Plus
// - Minus
// * Multiplication
// / Division
// % Remainder
// ** Power

console.log(5 + 2); // 7
console.log(5 - 2); // 3
console.log(5 * 2); // 10
console.log(5 / 2); // 2.5
console.log(5 % 2); // 1
console.log(5 ** 2); // 25 Power(es7)
console.log(Math.pow(5, 2)); //25 Power

